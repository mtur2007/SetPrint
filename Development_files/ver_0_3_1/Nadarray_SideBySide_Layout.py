# 各ブロックを行ごとにリストに変換
lines1 = block1.splitlines()
lines2 = block2.splitlines()

# 左側ブロックの最大文字数を取得し、各行をその幅に合わせる
max_len = max(len(line) for line in lines1)
lines1_padded = [line.ljust(max_len) for line in lines1]

# 両ブロックの行数が同じでない場合、短い方を空行で埋める
max_lines = max(len(lines1_padded), len(lines2))
while len(lines1_padded) < max_lines:
    lines1_padded.append(" " * max_len)
while len(lines2) < max_lines:
    lines2.append("")

# 行ごとに連結して横並びのテキストを生成（間にスペースを挟む）
combined = "\n".join(l1 + "  :  " + l2 for l1, l2 in zip(lines1_padded, lines2))
print(combined)
